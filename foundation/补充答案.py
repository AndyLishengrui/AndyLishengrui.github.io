def find_missing_answers(json_file):
    """找到缺失答案的经文引用"""
    try:
        with open(json_file, 'r', encoding='utf-8') as f:
            data = json.load(f)
        
        missing = []
        for key, answer in data.get('answers', {}).items():
            if not answer.get('has_data', False):
                missing.append((key, answer.get('reference', '')))
        
        return missing, data
    except Exception as e:
        print(f"读取文件失败 {json_file}: {e}")
        return [], None

def supplement_answers(json_file, max_supplements=5):
    """为指定文件补充答案"""
    missing, data = find_missing_answers(json_file)
    if not missing or not data:
        return 0
    
    supplemented = 0
    
    for key, reference in missing[:max_supplements]:
        # 清理引用格式，去除多余空格和标点
        clean_ref = reference.strip().replace('，', ',').replace('：', ':')
        
        # 尝试在经文数据库中查找
        found_text = None
        
        # 直接匹配
        if clean_ref in BIBLE_VERSES:
            found_text = BIBLE_VERSES[clean_ref]
        else:
            # 尝试模糊匹配
            for verse_ref, verse_text in BIBLE_VERSES.items():
                if verse_ref.replace(' ', '') == clean_ref.replace(' ', ''):
                    found_text = verse_text
                    break
        
        if found_text:
            data['answers'][key]['text'] = found_text
            data['answers'][key]['has_data'] = True
            supplemented += 1
            print(f"    补充: {reference} → {found_text[:30]}...")
    
    # 保存更新的数据
    if supplemented > 0:
        try:
            with open(json_file, 'w', encoding='utf-8') as f:
                json.dump(data, f, ensure_ascii=False, indent=2)
        except Exception as e:
            print(f"保存文件失败 {json_file}: {e}")
            return 0
    
    return supplemented

def main():
    """主函数"""
    answers_dir = Path(__file__).parent / 'data' / 'answers'
    
    # 获取所有答案JSON文件
    json_files = list(answers_dir.glob('foundation_L*.json'))
    json_files.sort()
    
    print("开始补充缺失的圣经经文答案...")
    print("=" * 60)
    
    total_supplemented = 0
    
    for json_file in json_files:
        missing, _ = find_missing_answers(json_file)
        if not missing:
            continue
            
        print(f"\n处理: {json_file.name} ({len(missing)}个缺失)")
        supplemented = supplement_answers(json_file, max_supplements=10)
        
        if supplemented > 0:
            print(f"  ✅ 补充了 {supplemented} 个答案")
            total_supplemented += supplemented
        else:
            print(f"  ➡️  暂无可补充的答案")
    
    print("=" * 60)
    print(f"\n总计补充了 {total_supplemented} 个圣经经文答案")
    
    if total_supplemented > 0:
        print("\n建议：运行 python3 检查答案.py 查看最新进度")

if __name__ == '__main__':
    main()